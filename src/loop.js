// 循环调用logic合约，检查是否执行下一步操作
function loop(delay) {
    var start = (new Date()).getTime();
    while((new Date()).getTime() - start < delay) {
    	continue;
    }

    var r = check_view();
    return r;
}

const DELAY = 1000;
async function run() {
	while(true) {
		if (true == loop(DELAY)) {
			await execute();			
		}
	}	
}

